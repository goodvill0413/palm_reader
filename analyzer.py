    def _preprocess_image(self, img: np.ndarray) -> Tuple[np.ndarray, np.ndarray]:
        """ì´ë¯¸ì§€ ì „ì²˜ë¦¬ - ì†ê¸ˆì„  ê²€ì¶œì„ ìœ„í•œ ìµœì í™”"""
        if len(img.shape) == 3:
            gray = cv2.cvtColor(img, cv2.COLOR_BGR2GRAY)
        else:
            gray = img.copy()
        
        # ë…¸ì´ì¦ˆ ì œê±°
